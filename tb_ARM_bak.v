`timescale 1ns/1ps

module tb_ARM;

    reg clk;
    reg rst;

    // ============================================================
    // DUT
    // ============================================================

    ARM dut(
        .clk(clk),
        .rst(rst)
    );

    // ============================================================
    // CLOCK GENERATION
    // ============================================================

    initial
    begin
        clk = 0;
        forever #5 clk = ~clk;
    end

    // ============================================================
    // RESET
    // ============================================================

    initial
    begin
        rst = 1;
        #20;
        rst = 0;
    end

    // ============================================================
    // MONITOR
    // ============================================================

    initial
    begin

        $display("====================================================");
        $display("TIME\tPC\tR0\tR1\tR2\tR3");
        $display("====================================================");

        $monitor(
            "%0t\t%0d\t%0d\t%0d\t%0d\t%0d",
            $time,

            dut.pc_addr,

            dut.rf.RF[0],
            dut.rf.RF[1],
            dut.rf.RF[2],
            dut.rf.RF[3]
        );

    end

    // ============================================================
    // SIMULATION END
    // ============================================================

    initial
    begin

        wait(dut.pc_addr >= 8'd6);

        #50;

        $display("====================================================");
        $display("FINAL REGISTER VALUES");
        $display("====================================================");

        $display("R0 = %0d", dut.rf.RF[0]);
        $display("R1 = %0d", dut.rf.RF[1]);
        $display("R2 = %0d", dut.rf.RF[2]);
        $display("R3 = %0d", dut.rf.RF[3]);
        $display("R4 = %0d", dut.rf.RF[4]);
        $display("R5 = %0d", dut.rf.RF[5]);
        $display("R6 = %0d", dut.rf.RF[6]);
        $display("R7 = %0d", dut.rf.RF[7]);

        $finish;

    end 

endmodule